        <h1>Maharashtra Kanya Van Samruddhi Yojana 2022</h1><br><br><br>
        <p>
            Maharashtra government has announced the launch of a new scheme Kanya Van Samruddhi Yojana 2022. Under this scheme, govt. will give plant saplings to each family of farmers where girl children is born. From now onward, any farmer’s family blessed with a girl child between 1 April to 31 March i.e anytime in an year can fill the application form to get plant saplings. This scheme will benefit around 2 lakh families of farmers per year.

            CM Devendra Fadnavis had earlier approved Maharashtra Kanya Van Samruddhi Yojana in the cabinet meeting held on 27 June 2018. This Kanya Van Samriddhi Scheme will also ensure that the income from these newly planted trees can be utilized to safeguard the future of girls. This scheme will result in women empowerment and will also promote tree plantation. However, this scheme is applicable for maximum 2 girl children in a family.
        </p>
        <h3>Maharashtra Kanya Van Samruddhi Yojana 2022</h3>
        <p>All the farmers can check the application form and features of this free plant saplings:-</p>
        <h4>Kanya Van Samruddhi Yojana Application Form</h4>
        <p>Any farmers family in the state blessed with a girl child between 1 April of the current year and 31 March of the next year can make registration. For this reason, farmers will have to submit an application form with the gram panchayat.</p>
        <img src="https://sarkariyojana.com/wp-content/uploads/2018/06/kanya-van-samriddhi-yojana-maharashtra.jpg" alt="kanya-van-samriddhi-yojana-maharashtra" width="80%">
        <p>Farmers will also have to produce the birth certificate of the girl children. After verification, each household will get 10 free of cost plant saplings from the forest department.</p>
        <h4>Kanya Van Samruddhi Yojana Features</h4>
        <p>The important features and highlights of this scheme are as follows:-</p>
        <ul>
            <li>Govt. will provide absolutely free plant saplings to all those families of farmers where girl is born.</li>
            <li>Each farmer will get 10 free of cost plant saplings.</li>
            <li>Beneficiaries are required to plant saplings between 1 July and 7 July.</li>
            <li>All the income earned from these trees will be utilized by the families to safeguard the future of girls.</li>
            <li>Plants saplings will be of different varieties – teakwood, mango, jackfruit, black plum and tamarind.</li>
            <li>Farmers can avail this benefit for maximum of 2 girls in a single family.</li>
        </ul>
        <p>Govt. is also going to assist all the beneficiaries under the Horticulture Scheme. Every year, approx. 2 lakh farmers families (10 lakh people) will get benefited from this scheme.</p>


