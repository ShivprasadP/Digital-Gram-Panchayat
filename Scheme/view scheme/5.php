        <h1>One Family One House Scheme </h1>
        <p>Government Of Maharashtra is going to start a new Housing Scheme i.e.<b> One Family One House Scheme</b> for the citizens of the state. Recently Bombay High Court has changed the eligibility criteria under the Various Government Housing Scheme and declared an order that a person will not be eligible to get a House/Flat in a government-regulated Housing scheme if he or his family member who has previously been allotted a flat anywhere in the state.</p>
        <img src="https://pmil.in/wp-content/uploads/2019/09/One-Family-One-House-Scheme.jpg" alt="One Family One House Scheme" width="80%">
        <h4>Maharashtra Ek Parivar Ek Ghar Yojana Details</h4>
        <p>Under <b>New One Family One House Scheme Maharashtra</b>Government Soon release a number of Housing Scheme in various places of the state. Previous year on 4th May 2019 in Bombay High Court a writ was filed. Under this filed writ it is mentioned that no other house will be allotted to a beneficiary if he has already one under the government scheme.</p>
        <h4>Decision of Court In One Family One House Scheme</h4>
        <p>Now on 2nd September 2019 Bombay High Court has changed the rule concerning the eligibility of beneficiaries in government housing schemes. then <b>‘one person, one house’</b> norm will be applied across Maharashtra. Now the Housing Department of Maharashtra is going to make all the rules, regulations and policy for the successful implementation of the scheme.</p>
