        <h4>Key features of the scheme</h4>
        <ol>
            <li>
                <p><b>Development of unorganised labours–</b> The state government had introduced the scheme to ensure that the workers in the unorganised sectors get the social and financial protections from the state government.</p>
            </li>
            <li>
                <p><b>Offer protection on-site</b> – After the implementation of this scheme, the beneficiaries will attain financial assistance if anything happens to them, while working on the project.</p>
            </li>
            <li>
                <p><b>Establishing work facilitation centres</b>– The scheme draft states that for the convenience of the workers and the better implementation of the scheme, the state government will establish 41 facilitation centres in various locations within the state.</p>
            </li>
            <li>
                <p><b>Current locations of the centres</b> – 8 facilitation centres have already been constructed by the state government. They are ready to start operations. These centres are located at Aurangabad, Worli, Jalna, Solapur, Panvel, Amravati, Bhandara and Nanded.</p>
            </li>
            <li>
                <p><b>Appointment of officials to monitor the labours’ conditions</b>– The state government will appoint 5 experienced specialists, who will ensure that the operation of the welfare scheme is going on as per the action plan.</p>
            </li>
            <li>
                <p><b>Number of registered workers</b>– The state reports highlight that 20.6 lakh labours, working in the unorganised sector are already registered with the Labour Welfare Board.</p>
            </li>
            <li>
                <p><b>Estimated beneficiary number</b> – When the scheme will be open for new registrations, the state government estimates that around 13.1 lakh new applicants will enrol for the welfare scheme.</p>
            </li>
            <li>
                <p><b>Contribution from the beneficiaries</b>– The scheme draft highlights that every registered labour will have to make a nominal contribution towards the benefit fund. The beneficiaries will have to deposit Re. 1 every month.</p>
            </li>
            <li>
                <p><b>Tenure of the contribution</b>– The project guidelines shed light on the fact that the registered applicant must continue this payment for 5 successive years. Thus, the total contribution amounts to Rs. 60.</p>
            </li>
            <li>
                <p><b>Validity of the smart card</b> – The Chief Minister has highlighted that once the smart card has been issued in the name of the beneficiary, it will remain valid for one year only. The smart card, issued by the state will contain a unique ID code for each applicant.</p>
            </li>
            <li>
                <p><b>Renewal process</b>– Once the smart card expires after the completion of one year, the beneficiary will be able to renew the pass. For this, he/she must log in on the official portal or go to the facilitation centres.</p>
            </li>
            <li>
                <p><b>Access to several benefits</b>– The state officials have highlighted that the scheme has been designed to offer the maximum protection to the construction labours. They will get financial assistance, accident benefits and other such perks.</p>
            </li>
        </ol>
        <h4>Eligibility for the applicants</h4>
        <ol aria-setsize="10px">
            <li>
                <p><b>Resident of Maharashtra</b>– It is mandatory for the labour to be a legal and permanent inhabitant of the state to secure the perks of this scheme.</p>
            </li>
            <li>
                <p><b>Must work in the unorganised sector </b>– Only such applicants will be allowed to acquire the benefits of this scheme who are directly associated with the unorganised categories.</p>
            </li>
            <li>
                <p><b>Category of the applicants</b>– The scheme will only allow those applicants who are associated with the construction works, drainage management, water supply and pipeline installation.</p>
            </li>
            <li>
                <p><b>Completion of 90 days</b>– If the applicant desires to become a beneficiary of the scheme, then he/she must have worked in the unorganised sector for three months in a row.</p>
            </li>
            <li>
                <p><b>Registered members only</b>– If the applicant wants to become a part of this scheme, then he must be registered with the Unorganised Labours Union in the state.</p>
            </li>
            <li>
                <p><b>Bank account is a must</b>– When it is time to get the financial assistance from the state, the applicant must have access to a bank account as all monetary depositions will be made via DBT mode.</p>
            </li>
        </ol>
        <h4>Documents necessary for application</h4>
        <ol>
            <li>
                <p><b>Residential proof documents</b>– If the candidate wants to receive the various benefits of this project, then he/she need to produce official residential documents, which shed light on the fact that the labour is a permanent and legal resident of Maharashtra.</p>
            </li>
            <li>
                <p><b>Aadhar card</b>– Every candidate needs to furnish a copy of his/her Aadhar card that will assist the government officials to scrutinise the details.</p>
            </li>
            <li>
                <p><b>Unorganised sector registration certificate</b>– It is necessary that the applicant submits a copy of the registration certificate that has been issued by the Unorganised labour Welfare Board.</p>
            </li>
            <li>
                <p><b>Work experience certificate</b>– During registration, the applicant needs to submit a document that has been issued by the construction manager that the candidate has been working on the project for three years.</p>
            </li>
            <li>
                <p><b>Bank account details</b>
                    <– Every interested applicant must submit the documents, which highlight the bank account number, bank and branch name, branch code and address, IFSC code etc, which make financial deposition ease.</p>
            </li>

        </ol>
        <h4>How to get registration form and enrol for the scheme?</h4>
        <p>The state government has mentioned the date from when the interested applicants will be able to log in on the official portal to complete the application process. But no details have been announced, which shed light on the official website of this scheme. The Maharashtra government will soon launch an authorised webpage. Apart from online application, the Chief Minister highlighted that the interested applicants can go to the facilitation centres for registration. The agents will help them with the enrolment. They will also issue the scheme smart card and hand it over to the applicant. For smart card issuance or renewal at these centres, the beneficiaries must pay Rs. 25 as a service fee. We will update the portal details and the online application process as soon as the government makes the official announcement.<br><br>The Chief Minister of the state was moved by the poor living and financial conditions of the unorganised sector labours. The implementation of this scheme was in his priority list. Finally, the state government has taken the necessary measures, and will be able to roll out the scheme in the beginning of the up-coming year. The various perks of the welfare project will offer the labours the much-needed social and economic securities, which they deserve.</p>

