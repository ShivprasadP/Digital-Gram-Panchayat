        <h4>Maharashtra Swadhar Yojana 2022 Eligibility</h4>
        <p>The candidates must fulfill the following eligibility criteria to become eligible for the Swadhar Yojana:-The candidates must fulfill the following eligibility criteria to become eligible for the Swadhar Yojana:-</p>
        <ul>
            <li>An applicant must be a permanent resident of Maharashtra and must belong to SC or Nav Bauddha Category as notified by the state government. </li>
            <li>The annual income of the family of the students from all sources must not exceed Rs. 2.5 lakh</li>
            <li>Student must possess a valid bank account linked to their Aadhaar Number.</li>
            <li>For pursuing professional courses after 10th, students should have taken admission in that course. Duration of course must not be less than 2 years.</li>
            <li>Students must have secured 60% marks in the previous examination</li>
            <li>All the physically challenged must have at-least 40% marks to qualify.</li>



