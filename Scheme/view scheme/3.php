        <h1>Maharashtra Swadhar Yojana 2022</h1><br><br><br>
        <p>Maharashtra govt. has launched Swadhar Yojana 2022 for the students of Scheduled Caste (SC) and Nav Bauddha category. Under this scheme, students will get Rs. 51,000 per year as assistance from govt. for their studies in Class 10th, 12th, degree, diploma & professional courses. This aid will be given for their lodging, boarding facilities and other expenses. Social Welfare Department of Maharashtra is implementing Maharashtra Swadhar Scheme for the welfare of poor and deprived candidates of SC and NB communities.</p>
